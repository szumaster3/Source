package core.plugin;

/**
 * The enum Plugin type.
 */
public enum PluginType {

    /**
     * Action plugin type.
     */
    ACTION,

    /**
     * Dialogue plugin type.
     */
    DIALOGUE,

    /**
     * Activity plugin type.
     */
    ACTIVITY,

    /**
     * Login plugin type.
     */
    LOGIN,

    /**
     * Logout plugin type.
     */
    LOGOUT,

    /**
     * Quest plugin type.
     */
    QUEST
}